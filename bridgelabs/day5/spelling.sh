read -p "Enter single digit value: " value

if [ $value -eq 1 ]
then
 echo "one"
elif [ $value  -eq 2 ]
then
  echo "two"
elif [ $value  -eq 3 ]
then
 echo "three"
elif [ $value  -eq 4]
then
  echo "four"
elif [ $value  -eq 5 ]
then
 echo "five"
elif [ $value  -eq 6 ]
then
  echo "six"
elif [ $value  -eq 7 ]
then
 echo "seven"
elif [ $value  -eq 8 ]
then
  echo "eight"
elif [ $value  -eq 9 ]
then
  echo "nine"
else
   echo "not single digit."

fi
